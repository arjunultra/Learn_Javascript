// Arithmatic operators
/*
    + Addition
    - Subtraction
    * Multiplication
    / Division
    % Reminder
    ** Power
    ++ Add one
    -- Subtract one
    */
//    Addition
let a = 10;
let b = 20;
let sum = a + b;
let diff = b - a;
let pdt = a * b;
let divide = b / a;
let reminder = b % a;
let power = a ** b;
console.log(sum);
console.log(diff);
console.log(pdt);
console.log(divide);
console.log(reminder);
console.log(power);
